const VI_MONTHS = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];

// "26 tháng 6, 2026" -> "Jun 26, 2026"
export function formatDateEn(viDate) {
  const match = viDate?.match(/(\d+)\s*tháng\s*(\d+),?\s*(\d+)/);
  if (!match) return viDate;
  const [, day, month, year] = match;
  return `${VI_MONTHS[Number(month) - 1]} ${day}, ${year}`;
}

